/// This class gives us the names
class japan

{ public:
  static std::string give_name(int index) 
{
    int surnames = 100;
    std::string ret_val;
    int s, g;
    s = index % surnames;
    g = (int) index/surnames;
    ret_val = JAPAN_given_name[g].name + " "+JAPAN_surname[s].name;
    ret_val += " ("+JAPAN_given_name[g].roman_name + " "+JAPAN_surname[s].roman_name+")";
   
    return ret_val;
  }


  static int select_name(double p) 
{
    // Takes a double uniformly distributed in [0,1)
    // enforce boundaries
    if (p < 0) return 0;
    if (p >= 1) return 42;

  int ret_val = (int) (99) * (100 )*p;

    // In theory, (int) p*x*y will never actually be x*y,
    return ret_val;
  }
};
